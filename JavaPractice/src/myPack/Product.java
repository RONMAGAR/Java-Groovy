package myPack;

public class Product {
    private int pid;
    private String pname;
    private float price;



    public void setPid(int pid) {
        this.pid = pid;
    }

    public int getPid() {
        return pid;
    }



    public void setPname(String pname) {
        this.pname = pname;
    }
    public String getPname() {
        return pname;
    }



    public void setPrice(float price) {
        this.price = price;
    }

    public float getPrice() {
        return price;
    }
}
